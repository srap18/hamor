UPDATE public.ship_catalog SET fishing_seconds = CASE sort_order
  WHEN 1 THEN 60     WHEN 2 THEN 90     WHEN 3 THEN 150    WHEN 4 THEN 210
  WHEN 5 THEN 240    WHEN 6 THEN 300    WHEN 7 THEN 330    WHEN 8 THEN 390
  WHEN 9 THEN 480    WHEN 10 THEN 600   WHEN 11 THEN 660   WHEN 12 THEN 780
  WHEN 13 THEN 840   WHEN 14 THEN 960   WHEN 15 THEN 1080  WHEN 16 THEN 1140
  WHEN 17 THEN 1260  WHEN 18 THEN 1380  WHEN 19 THEN 1500  WHEN 20 THEN 1620
  WHEN 21 THEN 1800  WHEN 22 THEN 1980  WHEN 23 THEN 2100  WHEN 24 THEN 2340
  WHEN 25 THEN 2520  WHEN 26 THEN 2700  WHEN 27 THEN 2940  WHEN 28 THEN 3120
  WHEN 29 THEN 3420  WHEN 30 THEN 3600
  ELSE fishing_seconds
END
WHERE sort_order BETWEEN 1 AND 30 AND code LIKE 'ship-lvl-%';